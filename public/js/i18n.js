let currentLang = localStorage.getItem('selectedLang') || 'en';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('selectedLang', lang);
  applyTranslations();

  // Update HTML lang attribute
  document.documentElement.lang = lang;
}

function applyTranslations() {
  const t = translations[currentLang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        // Preserve icons if they exist (assuming icons are before the text)
        const icon = el.querySelector('span, i, .emoji');
        if (icon) {
          // If there's an internal element, we might need more careful handling
          // For simplicity, if we find specific emoji/icons we keep them
        }
        el.textContent = t[key];
      }
    }
  });

  // Update lang buttons active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === currentLang);
  });

  // Dispatch event for other scripts (works.js, work-detail.js)
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: currentLang }));
}

// Global helper for scripts
window.getI18n = (key) => translations[currentLang]?.[key] || key;
window.getCurrentLang = () => currentLang;

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  document.documentElement.lang = currentLang;
});
